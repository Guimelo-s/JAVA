    private boolean cadastrarCliente(String titular, String agencia, String conta, String senha, double saldo) {
    String query = "INSERT INTO clientes (agencia, conta, senha, titular, saldo) VALUES (?, ?, ?, ?, ?)";
    try (Connection con = DataBaseConnection.getConnection();
         PreparedStatement pst = con.prepareStatement(query)) {

        pst.setString(1, agencia);
        pst.setString(2, conta);
        pst.setString(3, senha);
        pst.setString(4, titular);
        pst.setDouble(5, saldo);
        pst.executeUpdate();
        return true;
    } catch (SQLException e) {
        e.printStackTrace();
        return false;
    }
}
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {                                           
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String conta = (String) jTable1.getValueAt(selectedRow, 2);
            if (excluirCliente(conta)) {
                JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso!");
                atualizarTabelaClientes();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir cliente.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para excluir.");
        }
    }            
    private boolean excluirCliente(String conta) {
        String query = "DELETE FROM clientes WHERE conta = ?";
        try (Connection con = DataBaseConnection.getConnection();
             PreparedStatement pst = con.prepareStatement(query)) {

            pst.setString(1, conta);
            pst.executeUpdate();
            return true;
        } catch (SQLException e) {
            e.printStackTrace();
            return false;
        }
    }
      
     
