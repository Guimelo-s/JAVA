    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String titular = txtTitularColab.getText();
        String agencia = txtAgenciaColab.getText();
        String conta = txtContaColab.getText();
        String senha = txtSenhaColab.getText();
        double saldo = Double.parseDouble(txtSaldoColab.getText());

    if (cadastrarCliente(titular, agencia, conta, senha, saldo)) {
        JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
        atualizarTabelaClientes();
        limparCampos();
    } else {
        JOptionPane.showMessageDialog(this, "Erro ao cadastrar cliente.");
    }
    }//GEN-LAST:event_btnCadastrarActionPerformed

