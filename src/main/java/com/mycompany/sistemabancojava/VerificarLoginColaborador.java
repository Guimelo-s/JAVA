   private void btnLoginGerenteActionPerformed(java.awt.event.ActionEvent evt) {                                               
        String idGerente = txtIdGerente.getText();
        String senha = txtSenhaGerente.getText();

        if (verificarLoginGerente(idGerente, senha)) {
            JOptionPane.showMessageDialog(this, "Login bem-sucedido!");
            new TelaLoginColaborador().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "ID ou senha incorretos.");
        }
    }           
   private boolean verificarLoginGerente(String idGerente, String senha) {
        String query = "SELECT * FROM gerentes WHERE id_gerente = ? AND senha = ?";
        try (Connection con = DataBaseConnection.getConnection();
             PreparedStatement pst = con.prepareStatement(query)) {

            pst.setString(1, idGerente);
            pst.setString(2, senha);
            try (ResultSet rs = pst.executeQuery()) {
                return rs.next();
            }
        } catch (SQLException e) {
            e.printStackTrace();
            return false;
        }
    }  
   
